        $(function(){
            
            var acoodion = function() {
                var me = $(this);
                var bros = $(this).siblings();
                
                if( me.hasClass('is-active')) {
                    me.removeClass('is-active');
                    me.find('.add').text('add');
                    me.find('.check').removeClass('is-active').text('check_box_outline_blank');
                    me.find('.sub-txt').slideUp();
                } else {
                    me.addClass('is-active');
                    me.find('.add').text('remove');
                    me.find('.check').addClass('is-active').text('check_box');
                    bros.removeClass('is-active');
                    
                    me.find('.sub-txt').slideDown();
                    bros.find('.sub-txt').slideUp();
                    bros.find('.add').text('add');
                    bros.find('.check').removeClass('is-active').text('check_box_outline_blank');
                }
            };
            
            $('.schedulelist .items').on('click', '.item', acoodion);
