    <script type="text/javascript">
        $(function(){
            var listAll = $("input[type='checkbox'].agreechkAll");
            var list = $("input[type='checkbox'].chack");  

            var checkbox = {
                allcheck : function(e) {
                    if($(e.target).prop("checked") === true) { 
                        //해당화면에 전체 checkbox들을 체크한다 
                        console.log($(e.target))
                        list.prop("checked",true);
                        list.closest('.check-box__component').find('.material-icons').text('check_box');
                        listAll.closest('.check-box__component').find('.material-icons').text('check_box');
                    } else if($(e.target).prop("checked") === false){
                        //해당화면에 모든 checkbox들의 체크를해제 한다. 
                        list.prop("checked",false);
                        list.closest('.check-box__component').find('.material-icons').text('check_box_outline_blank');
                        listAll.closest('.check-box__component').find('.material-icons').text('check_box_outline_blank');
                    }
                },

                chack : function(e) {
                    if($(e.target).prop("checked") === true){
                        $(e.target).prop("checked", true)
                        $(e.currentTarget).next('.check-box__inner').find('.material-icons').text('check_box');
                    }
                    if($(e.target).prop("checked") === false){
                        $(e.target).prop("checked", false)
                        listAll.prop('checked', false);
                        $(e.currentTarget).next('.check-box__inner').find('.material-icons').text('check_box_outline_blank');
                    }
                    // 각자 하나씩 선택시 자동으로 전채선태도 활성화 시키기
                    var size = list.filter(':checked').length;
                    if(size == list.length) {
                        listAll.prop("checked", true);
                        listAll.next('.check-box__inner').find('.material-icons').text('check_box');
                    } else {
                        listAll.prop("checked", false);
                        listAll.next('.check-box__inner').find('.material-icons').text('check_box_outline_blank');
                    }
                },
            }
            $('.propslist').on('click', '.agreechkAll', checkbox.allcheck)
            $('.propslist').on('click', '.chack', checkbox.chack)
        });
   
    </script>
